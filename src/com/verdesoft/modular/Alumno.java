package com.verdesoft.modular;

public class Alumno {
	String nombre;
	int edad;
	int curso;
	double notaMedia;
	
	/**
	 * Constructor de la clase Alumno
	 * @param nombre
	 * @param edad
	 * @param curso
	 * @param notaMedia
	 */
	public Alumno(String nombre, int edad, int curso, double notaMedia) {
		this.nombre = nombre;
		this.edad = edad;
		this.curso = curso;
		this.notaMedia = notaMedia;
	}
	
	

}
